    private void CompletarTabla() {

        DefaultTableModel modelo = (DefaultTableModel) jtbl_verClientes.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);      //Para borrar los registros anteriores     
        }
        for (int i = 1; i <= Opera.NroReg-1; i++) {
            modelo.addRow(new Object[]{Opera.Cuenta[i].NombreUsuario, Opera.Cuenta[i].Rut, Opera.Cuenta[i].Contraseña, Opera.Cuenta[i].SaldoAhorro,
                Opera.Cuenta[i].SaldoCorriente});

        }

    }

