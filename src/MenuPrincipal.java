    private void jbtn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_registrarActionPerformed

        if (this.jtxt_nombreClientetabla.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Falta Nombre del Cliente");
        } else if (this.jtxt_rutClientetabla.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Falta Rut del cliente");
        } else if (this.jtxt_contraseñaCliTabla.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Falta Contraseña");
        } else if (this.jtxt_ctaAhorrotabla.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Falta Ingresar Monto");
        } else if (this.jtxt_ctaCorrientetabla.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Falta Ingresar Monto");
        } else {
            Opera.IngresarCliente(this.jtxt_nombreClientetabla.getText(), this.jtxt_rutClientetabla.getText(), this.jtxt_contraseñaCliTabla.getText(), Integer.parseInt(this.jtxt_ctaAhorrotabla.getText()), Integer.parseInt(this.jtxt_ctaCorrientetabla.getText()));

            CompletarTabla();
        }

    }//GEN-LAST:event_jbtn_registrarActionPerformed


